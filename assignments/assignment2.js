
function quotient(x, y) {
    return x / y;
}

console.log("The quotient of 12 / 5 is " + quotient(12, 5))



function sumFirstNIntegers(n) {
    return (n * (n + 1)) / 2 
}

console.log("The sum of the integer 100 is " + sumFirstNIntegers(100))


function aOFt(b, h) {
    return (b * h) / 2
}

